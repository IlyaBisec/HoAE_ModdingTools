int CreateCone(    int    hSeg, int cSeg,    float height, float    topR, float    botR,    
                VertCallback putVert, PolyCallback    putPoly, ShapeAction action )
{
    if (action == scGetNVert) return (cSeg + 1)*(hSeg + 1);
    if (action == scGetNPoly) return cSeg*hSeg*2;
    
    //  vertices
    float    dPhi= c_DoublePI/float( cSeg );
    float    z    = 0.0f;
    float    k   = (topR - botR)/height;
    float    dz    = height/float( hSeg );
    float    v    = 1.0f;
    float    dv    = 1.0f / float( hSeg );
    float    du    = 1.0f / float( cSeg );
    for (int i = 0; i <= hSeg; i++)
    {
        float phi = 0.0f;
        float u = 0.0f;
        float r = k*z + botR;
        for (int j = 0; j <= cSeg; j++)
        {    
            float x = cosf( phi )*r;
            float y = sinf( phi )*r;
            putVert( x, y, z, u, v );
            phi += dPhi;
            u += du;
        }
        z += dz;
        v -= dv;
    }

    //  polygons
    int    cV = 0;
    for (int i = 0; i < hSeg; i++)
    {
        for (int j = 0; j < cSeg; j++)
        {    
            int nV = cV + cSeg + 1;
            putPoly( cV, cV + 1, nV + 1 );
            putPoly( cV, nV + 1, nV );
            cV++;
        }
        cV++;
    }

    return 1;
} // CreateCone

